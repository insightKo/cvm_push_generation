def _strip_suffix(extern_id: str) -> str:
    """Убрать '_0' в конце ID."""
    if not isinstance(extern_id, str):
        return str(extern_id)
    return re.sub(r"_0$", "", extern_id.strip())


def load_assortment() -> pd.DataFrame:
    df = pd.read_excel(ASSORT_FILE, sheet_name="Лист1")
    df["PRODUCT_NAME"] = df["PRODUCT_NAME"].astype(str)
    df["PRODUCT_NAME_U"] = df["PRODUCT_NAME"].str.upper()
    # Агрегируем по уникальному товару (один и тот же ID встречается в разных городах)
    agg = (
        df.groupby(
            ["PRODUCT_NAME", "PRODUCT_NAME_U", "ID_PRODUCT_EXTERNAL",
             "ID_CATEGORY_5_ext", "ID_CATEGORY_4_ext", "ID_CATEGORY_3_ext"],
            as_index=False,
        )
        .agg(
            REAL_SALE=("REAL_SALE", "sum"),
            COUNT_CHECK=("COUNT_CHECK", "sum"),
            AVG_PRICE=("AVG_PRICE", "mean"),
            PENETRATION_CHECK=("PENETRATION_CHECK", "mean"),
        )
        .sort_values("PENETRATION_CHECK", ascending=False)
    )
    return agg.reset_index(drop=True)


def find_products(agg: pd.DataFrame, spec: dict) -> list[dict]:
    cats = spec.get("cats", [])
    starts_with = [s.upper() for s in spec.get("starts_with", [])]
    exclude = [s.upper() for s in spec.get("exclude", [])]
    n = spec.get("n", 3)

    sub = agg
    if cats:
        sub = sub[sub["ID_CATEGORY_5_ext"].isin(cats)]
    if starts_with:
        mask = pd.Series(False, index=sub.index)
        for p in starts_with:
            mask = mask | sub["PRODUCT_NAME_U"].str.startswith(p, na=False)
        sub = sub[mask]
    for ex in exclude:
        sub = sub[~sub["PRODUCT_NAME_U"].str.contains(re.escape(ex), na=False, regex=True)]

    sub = sub.head(n)
    out = []
    for _, r in sub.iterrows():
        out.append(
            {
                "name": r["PRODUCT_NAME"],
                "id": _strip_suffix(r["ID_PRODUCT_EXTERNAL"]),
                "cat5": int(r["ID_CATEGORY_5_ext"]) if pd.notna(r["ID_CATEGORY_5_ext"]) else None,
                "avg_price": round(float(r["AVG_PRICE"]), 2) if pd.notna(r["AVG_PRICE"]) else None,
                "penetration": round(float(r["PENETRATION_CHECK"]), 5),
            }
        )
    return out


def build() -> dict:
    agg = load_assortment()
    result = {}
    for recipe, ingredients in RECIPES.items():
        recipe_items = []
        for spec in ingredients:
            prods = find_products(agg, spec)
            recipe_items.append({"ingredient": spec["label"], "products": prods})
        result[recipe] = recipe_items
    return result


def main():
    data = build()
    OUT_FILE.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Saved: {OUT_FILE}")
    for recipe, ingredients in data.items():
        total = sum(len(it["products"]) for it in ingredients)
        empty = sum(1 for it in ingredients if not it["products"])
        print(f"  {recipe:20s}  ingredients={len(ingredients):2d}  products={total:3d}  empty={empty}")


if __name__ == "__main__":
    main()
